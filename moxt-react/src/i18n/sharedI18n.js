export function sharedText(t, key, vars = {}) {
  const translated = typeof t === 'function' ? t(key, vars) : null
  if (translated && translated !== key) return translated
  const source = SHARED_FR_SOURCES[key]
  if (!source) return key
  return source.replace(/\{(\w+)\}/g, (match, name) =>
    Object.prototype.hasOwnProperty.call(vars, name) ? String(vars[name]) : match,
  )
}

export function sharedOptionLabel(t, option) {
  if (!option) return ''
  return option.labelKey ? sharedText(t, option.labelKey) : option.label || ''
}
