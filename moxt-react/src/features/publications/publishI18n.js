export function publishText(t, key, vars = {}) {
  const translated = typeof t === 'function' ? t(key, vars) : key
  if (translated !== key) return translated
  const source = PUBLISH_FR_SOURCES[key]
  if (!source) return key
  return source.replace(/\{(\w+)\}/g, (match, name) =>
    Object.prototype.hasOwnProperty.call(vars, name) ? String(vars[name]) : match,
  )
}

export function publishOptionLabel(t, option) {
  if (!option) return ''
  return option.labelKey ? publishText(t, option.labelKey) : option.label || ''
}

export function publishOptionSub(t, option) {
  if (!option) return ''
  return option.subKey ? publishText(t, option.subKey) : option.sub || ''
}
