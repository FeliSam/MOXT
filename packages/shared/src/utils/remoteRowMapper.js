export function fromRow(row) {
  if (!row || typeof row !== 'object') return row
  const result = {}
  for (const [key, value] of Object.entries(row)) {
    result[camelMap[key] ?? key] = value
  }
  return result
}

export function fromRows(rows) {
  return (rows || []).map(fromRow)
}
